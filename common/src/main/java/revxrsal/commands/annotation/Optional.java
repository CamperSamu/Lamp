package revxrsal.commands.annotation;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Marks a parameter as optional and does not need to be explicitly specified by
 * the sender.
 * <p>
 * <p>
 * Note that certain parameters may be marked as optional even if this annotation
 * is not present. As of now, these will always be marked as {@link Optional}:
 * <ul>
 *     <li>A parameter with {@link Default} on it</li>
 *     <li>A parameter with {@link Sized} such that {@link Sized#min()} == 0</li>
 * </ul>
 */
@Target(ElementType.PARAMETER)
@Retention(RetentionPolicy.RUNTIME)
@NotSender.ImpliesNotSender
public @interface Optional {
}
